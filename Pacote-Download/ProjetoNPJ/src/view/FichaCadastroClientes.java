    // METÓDO PARA APAGAR A PASTA DO USUÁRIO
    public void removerArquivos(File f) {
     if (f.isDirectory()) {
        File[] files = f.listFiles();
        for (File file : files) {
            removerArquivos(file);
         }
     }
     f.delete();
  }
    

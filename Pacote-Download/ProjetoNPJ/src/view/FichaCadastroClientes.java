    private void imprimirFicha(){
        int confirma = JOptionPane.showConfirmDialog(null, "Confirma a impressão do relatório? ", "Atenção",JOptionPane.YES_NO_OPTION);
        if (confirma == JOptionPane.YES_OPTION){
            try {
            HashMap filtro = new HashMap();
            filtro.put("id_cliente", Integer.parseInt(jTextFieldIdCliente.getText())); //C:\\Users\\Nilma\\Desktop\\ProjetoNPJ\\FichaClientes\\FichaCliente.jasper
            JasperPrint print = JasperFillManager.fillReport("C:\\FichaClientes\\FichaCliente.jasper",filtro,getConnection());
            JasperViewer.viewReport(print, false);
            } catch (JRException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao imprimir "+ex);
            }
        }
    }
    

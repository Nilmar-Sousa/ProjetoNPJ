    //  METÓDO PARA PREENCHER A TABELA CLIENTES
    public void preencheTabela(String sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID do Cliente","Nome","CPF"};
        conex.getConnection();
        conex.executasql(sql);
        try{
            conex.rs.first();
            do{
            dados.add(new Object[]{conex.rs.getInt("id_cliente"),conex.rs.getString("nome_cliente"),conex.rs.getString("cpf_cliente")});
            }while(conex.rs.next());
        }catch(SQLException ex){
        JOptionPane.showMessageDialog(rootPane, "Busque por outro cliente para preencher a tabela");
    }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTable_Clientes.setModel(modelo);
        jTable_Clientes.getColumnModel().getColumn(0).setPreferredWidth(92);
        jTable_Clientes.getColumnModel().getColumn(0).setResizable(false);
        jTable_Clientes.getColumnModel().getColumn(1).setPreferredWidth(440);
        jTable_Clientes.getColumnModel().getColumn(1).setResizable(false);
        jTable_Clientes.getColumnModel().getColumn(2).setPreferredWidth(250);
        jTable_Clientes.getColumnModel().getColumn(2).setResizable(false);
        jTable_Clientes.getTableHeader().setReorderingAllowed(false);
        jTable_Clientes.setAutoResizeMode(jTable_Clientes.AUTO_RESIZE_OFF);
        jTable_Clientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconectar();
    }
    
